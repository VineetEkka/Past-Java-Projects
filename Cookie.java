public class Cookie {
    private String flavor;
    public Cookie(String f){
        flavor=f;
    }
    public Cookie(){
        flavor="";
    }
    public void setFlavor(String f){
        flavor=f;
    }
    public String getFlavor(String f){
        return f;
    }
}
